


/**
 * UI exception is thrown when there is an error in the UI component of the classification of new Documents 

 * @author Rachel Warren 
 * @version 12-10-2013
 */
public class UIException extends Exception
{
    
    public UIException() {
        super(); 
    } 
    
    public UIException(String message) { 
        super(message); 
    } 
    
    public UIException(String message, Throwable cause) { 
        super(message, cause); 
    } 
    
    public UIException(Throwable cause) { 
        super(cause); 
    } 
} 
    

